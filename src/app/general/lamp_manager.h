#ifndef LAMP_MANAGER_H
#define LAMP_MANAGER_H


/*******************************************************************
** 函数名:      lamp_manager_init
** 函数描述:    lamp初始化
** 参数:        无
** 返回:        无
********************************************************************/
void lamp_manager_init(void);

/*******************************************************************
** 函数名:      lamp_event_create
** 函数描述:    创建一个lamp event
** 参数:        [in] lamp    : 灯具编号
**              [in] event   : 控制事件指针
** 返回:        true or false
********************************************************************/
BOOLEAN lamp_event_create(Lamp_e lamp, Lamp_event_t *event);

/*******************************************************************
** 函数名:      lamp_ctl
** 函数描述:    删除一个lamp event
** 参数:        [in] lamp    : 灯具编号
**              [in] event   : 控制事件
** 返回:        true or false
********************************************************************/
BOOLEAN lamp_event_delete(Lamp_e lamp, Lamp_envnt_e event);

/*******************************************************************
** 函数名:      lamp_get_status
** 函数描述:    获取指定灯号当前的状态
** 参数:        [in] lamp    : 灯具编号
** 返回:        Lamp_tvent_t
********************************************************************/
Lamp_event_t* lamp_get_status(Lamp_e lamp);

/*******************************************************************
** 函数名:      lamp_is_open
** 函数描述:    判断灯开/关状态
** 参数:        [in] lamp:
** 返回:        true or false
********************************************************************/
BOOLEAN lamp_is_open(Lamp_e lamp);

/*******************************************************************
** 函数名:      lamp_get_dimming
** 函数描述:    获取调光值
** 参数:        [in] lamp:
** 返回:        dimming
********************************************************************/
INT8U lamp_get_dimming(Lamp_e lamp);

#endif

