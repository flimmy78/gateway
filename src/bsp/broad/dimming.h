#ifndef DIMMING_H
#define DIMMING_H


/*******************************************************************
** 函数名:      dimming_init
** 函数描述:    初始化调光模块
** 参数:        none
** 返回:        none
********************************************************************/
void dimming_init(void);

/*******************************************************************
** 函数名:      pwm_dimming
** 函数描述:    pwm方式调光调用接口
** 参数:        [in] chn : 通道号
**              [in] dim : 调光值
** 返回:        true or false
********************************************************************/
uint8_t pwm_dimming(uint8_t chn, uint8_t dim);

/*******************************************************************
** 函数名:      dac_dimming
** 函数描述:    dac方式调光调用接口
** 参数:        [in] chn : 通道号
**              [in] dim : 调光值
** 返回:        true or false
********************************************************************/
uint8_t dac_dimming(uint8_t chn, uint8_t dim);

#endif

